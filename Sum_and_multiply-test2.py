def multiplyList(myList):
    result = 1
    for x in myList:
        result = result * x
    return result


list1 = [1, 2, 3, 4]
print(multiplyList(list1))

def sumlist (myList2):
    result = 1
    for y in myList2:
        result = result + y
    return result

list2 = [1, 2, 3, 4]
print(sumlist(list2))
